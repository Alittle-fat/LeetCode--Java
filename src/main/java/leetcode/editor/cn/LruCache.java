    class LRUCache {
        int capacity;
        HashMap<Integer,Integer> map;
        LinkedList<Integer> list;
    public LRUCache(int capacity) {
        //åˆå§‹åŒ–
        this.capacity = capacity;
        map = new HashMap<>();
        list = new LinkedList<>();
    }
    
    public int get(int key) {
        if(map.containsKey(key)){
            //è·Ÿæ–°æ“ä½œè®°å½•,å°†keyæ”¾åœ¨listçš„æœ€æœ«å°¾
            list.remove((Integer)key);
            list.addLast(key);
            return map.get(key);
        }else{
            return -1;
        }
    }
    
    public void put(int key, int value) {
        if (map.containsKey(key)){
            //å·²ç»å­˜åœ¨,ç›´æ¥æ›´æ–°
            map.put(key,value);
            //ä¸€å®šè¦å¼ºåˆ¶ç±»å‹è½¬æ¢
            list.remove((Integer)key);
            list.addLast(key);
        }else if(list.size()==capacity){
            //è¾¾åˆ°å®¹é‡ä¸Šé™,åˆ é™¤æœ€æ—§çš„æ•°æ®
            map.remove(list.removeFirst());
            map.put(key,value);
            list.addLast(key);
        }else{
            map.put(key,value);
            list.addLast(key);
        }
    }
}

